import { supabaseServer } from '@/lib/supabase/server';
import DesignGallery from '@/components/DesignGallery';
import Header from '@/components/Header';
import Footer from '@/components/Footer';
import EzoicPlacements from '@/components/EzoicPlacements';

export const revalidate = 0;

export default async function HomePage({
  searchParams,
}: {
  searchParams: { category?: string };
}) {
  const category = searchParams?.category;

  let query = supabaseServer
    .from('designs')
    .select('*')
    .order('created_at', { ascending: false })
    .range(0, 11);

  if (category) {
    query = query.eq('category', category);
  }

  const { data: initialDesigns } = await query;

  return (
    <div className="min-h-screen bg-luxury-white">
      <Header selectedCategory={category || null} />

      <main className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-16">
        <section className="mb-16 space-y-6 text-center md:text-left">
          <p className="text-xs uppercase tracking-[0.35em] text-gray-500">
            United States Â· AI Product Design
          </p>
          <h1 className="text-4xl md:text-5xl font-semibold text-gray-900 tracking-tight">
            Production-ready AI web design inspiration for modern engineering teams
          </h1>
          <p className="text-lg text-gray-600 max-w-3xl mx-auto md:mx-0">
            UI Syntax curates Silicon Valley standard landing pages, dashboards, and e-commerce layouts
            generated by AI. Every concept is reviewed with accessibility guidelines, marketing
            copy, and developer handoff in mind so your next release ships faster.
          </p>
          <div className="flex flex-wrap justify-center md:justify-start gap-3">
            {["B2B SaaS", "Developer tools", "Fintech", "Commerce"].map((tag) => (
              <span
                key={tag}
                className="rounded-full border border-gray-900 px-4 py-1 text-sm font-medium text-gray-800 bg-white"
              >
                {tag}
              </span>
            ))}
          </div>
        </section>

        {/* Replace placement IDs with values from the Ezoic dashboard */}
        <EzoicPlacements placementIds={[101, 102, 103]} wrapperClassName="my-16 space-y-8" />

        <DesignGallery initialDesigns={initialDesigns || []} initialCategory={category || null} />
      </main>

      <Footer />
    </div>
  );
}
